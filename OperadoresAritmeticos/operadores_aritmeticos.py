
a=10;
b=3;

suma= a+b
print(f'Suma: {suma}')

resta=a-b
print(f'Resta: {resta}')

multiplicacion= a*b
print(f'Multiplicacion: {multiplicacion}')

division= a/b
print(f'Division: {division:.2f}') #.2f sirve para poner el numero de decimales

division_entera= a//b
print(f'Division entera: {division_entera}')

modulo= a%b #Residuo de la division
print(f'Residuo de la division: {modulo}')

exponente= a**b #10 elevado a 3 en este caso
print(f'Exponente: {exponente}')


